-- Fix duplicate foreign key constraints that are causing ingredients query to fail
-- The error shows there are two relationships between recipe_ingredients and ingredients

-- First, let's see what constraints exist
SELECT 
    tc.table_name, 
    tc.constraint_name, 
    tc.constraint_type,
    kcu.column_name,
    ccu.table_name AS foreign_table_name,
    ccu.column_name AS foreign_column_name
FROM 
    information_schema.table_constraints AS tc 
    JOIN information_schema.key_column_usage AS kcu
      ON tc.constraint_name = kcu.constraint_name
      AND tc.table_schema = kcu.table_schema
    JOIN information_schema.constraint_column_usage AS ccu
      ON ccu.constraint_name = tc.constraint_name
      AND ccu.table_schema = tc.table_schema
WHERE tc.constraint_type = 'FOREIGN KEY' 
  AND tc.table_name = 'recipe_ingredients'
  AND ccu.table_name = 'ingredients';

-- Drop the duplicate constraint (the one with space in name)
ALTER TABLE recipe_ingredients 
DROP CONSTRAINT IF EXISTS "recipe ingredients_ingredient_id_fkey";

-- Verify only one constraint remains
